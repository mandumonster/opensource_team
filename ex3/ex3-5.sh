#!/bin/bash

# 함수 정의
list_files() {
    echo "함수 안으로 들어왔음"
    
    # -l 옵션을 확인할 때, 조건문을 제대로 작성
    if [ "$1" = "-l" ]; then
        ls -l  # -l 옵션을 입력받으면 'ls -l' 실행
    else
        ls  # 아무 옵션을 입력하지 않으면 'ls' 실행
    fi
}

# 프로그램 시작
echo "프로그램을 실행합니다."
list_files "$1"  # 첫 번째 인자를 함수에 전달 (옵션으로 사용)
echo "프로그램을 종료합니다."

